public class B3_C_Dimensions {
    // This class will be used in body as a part of it.
    private double height;
    private double width;
    private double depth;

    public B3_C_Dimensions(double height, double width, double depth) {
        this.height = height;
        this.width = width;
        this.depth = depth;
    }

    public double getHeight() {
        return height;
    }

    public double getWidth() {
        return width;
    }

    public double getDepth() {
        return depth;
    }
}
